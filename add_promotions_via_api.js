async function addPromotionsViaAPI() {
  console.log('üöÄ Inizio aggiunta promozioni tramite API...\n');
  
  let successCount = 0;
  let errorCount = 0;

  for (const promo of promotions) {
    try {
      console.log(`Aggiungendo: ${promo.title}...`);
      
      // Nota: Dovrai essere loggato o usare un token admin
      // Per ora questo script presuppone che tu abbia un utente loggato
      
      const response = await axios.post(`${BASE_URL}/admin/promotions`, promo);
      
      if (response.status === 201) {
        console.log(`‚úÖ ${promo.title} aggiunta con successo`);
        successCount++;
      }
    } catch (error) {
      console.error(`‚ùå Errore aggiungendo ${promo.title}:`, error.response?.data || error.message);
      errorCount++;
    }
  }

  console.log(`\nüéâ Completato!`);
  console.log(`‚úÖ Aggiunte: ${successCount}`);
  console.log(`‚ùå Errori: ${errorCount}`);
}

